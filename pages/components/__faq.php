			<article class="faq__item" itemscope itemprop="mainEntity" itemtype="https://schema.org/Question">
				<div class="faq__question">
					<h4 itemprop="name">Comment vais-je gérer la fatigue et les courbatures ?</h4>
					<button class="faq__btn-more" aria-expanded="false"></button>
				</div>
				<div class="faq__answer" itemscope itemprop="acceptedAnswer" itemtype="https://schema.org/Answer">
					<p itemprop="text">Pour mieux gérer la fatigue et les courbatures, il est essentiel d’inclure des périodes de récupération dans votre routine d'entraînement.</p>
					<p itemprop="text">Assurez-vous de bien vous hydrater, de pratiquer des étirements après chaque séance et d’intégrer des techniques comme le massage ou les bains chauds pour soulager les tensions musculaires.</p>
					<p itemprop="text">Une bonne qualité de sommeil et une alimentation riche en protéines et en nutriments essentiels contribuent également à une meilleure récupération.</p>
				</div>
			</article>

			<article id="faq__item-last" class="faq__item" itemscope itemprop="mainEntity" itemtype="https://schema.org/Question">
				<div class="faq__question">
					<h4 itemprop="name">Que dois-je changer dans mon alimentation pour maximiser les résultats ?</h4>
					<button class="faq__btn-more" aria-expanded="false"></button>

				</div>
				<div class="faq__answer" itemscope itemprop="acceptedAnswer" itemtype="https://schema.org/Answer">
					<p itemprop="text">Pour optimiser vos résultats, privilégiez une alimentation équilibrée comprenant des protéines de qualité (viandes maigres, poissons, œufs, légumineuses), des glucides complexes (riz complet, patates douces, quinoa) et des lipides sains (avocats, noix, huile d'olive).</p>
					<p itemprop="text">Pensez également à bien vous hydrater et à ajuster votre apport calorique en fonction de votre niveau d'activité physique.</p>
					<p itemprop="text">Les suppléments comme la whey protéine, les BCAA ou la créatine peuvent aussi être utiles selon vos objectifs.</p>
				</div>
