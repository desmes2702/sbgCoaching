					<h4 itemprop="name">Combien de temps faudra-t-il pour voir des résultats ?</h4>
					<button class="faq__btn-more" aria-expanded="false"></button>

				</div>
				<div class="faq__answer" itemscope itemprop="acceptedAnswer" itemtype="https://schema.org/Answer">
					<p itemprop="text">La durée nécessaire pour voir des résultats dépend de plusieurs facteurs, notamment de votre niveau de forme physique actuel, de la fréquence et de l'intensité de vos entraînements, ainsi que de votre alimentation.</p>
					<p itemprop="text">En général, vous pouvez commencer à remarquer des améliorations en termes d'énergie, d'humeur et de condition physique après 2 à 4 semaines d'entraînement régulier.</p>
